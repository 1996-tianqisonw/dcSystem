package com.hx.entity;

public class SystemParameters {
    private Integer stId;

    private String stField;

    private String stValue;

    private String stText;

    private String stType;

    public Integer getStId() {
        return stId;
    }

    public void setStId(Integer stId) {
        this.stId = stId;
    }

    public String getStField() {
        return stField;
    }

    public void setStField(String stField) {
        this.stField = stField;
    }

    public String getStValue() {
        return stValue;
    }

    public void setStValue(String stValue) {
        this.stValue = stValue;
    }

    public String getStText() {
        return stText;
    }

    public void setStText(String stText) {
        this.stText = stText;
    }

    public String getStType() {
        return stType;
    }

    public void setStType(String stType) {
        this.stType = stType;
    }
}